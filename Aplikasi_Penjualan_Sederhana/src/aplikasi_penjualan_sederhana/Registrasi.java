    private void regisbtn_inActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regisbtn_inActionPerformed
        // TODO add your handling code here:
        try{
            user u = new user();
            this.stat = k.getCon().prepareStatement("insert into user values(?,?,?,?,?)");
            stat.setInt(1, 0);  //artinya kolom ke 1 di nol kan,
                                //karena kolom ke 1 adalah id_user yang aouto increment
            stat.setString(2, u.username); // artinya kolom 2 merupakan username seusai yang di db
            stat.setString(3, u.password);
            stat.setString(4, u.nama_user);
            stat.setInt(5, u.id_level);
            //merupakn data dari table GUI ke sql menggunakan executeupdate
            stat.executeUpdate();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_regisbtn_inActionPerformed

