    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID User");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("Nama User");
        model.addColumn("ID Level");
        regis_tbl.setModel(model);
        //setiap mengambil data dari sql diawali dengan try
        try{
            this.stat = k.getCon().prepareStatement("select * from user");
            this.rs = this.stat.executeQuery(); // mengambil data dari sql ke table GUI
            //karena pasti bukan hanya satu user saja maka digunakanlah perulangan
            while (rs.next()){
                Object[] data ={
                    rs.getString("id_user"),
                    rs.getString("username"),
                    rs.getString("password"),
                    rs.getString("nama_user"),
                    rs.getString("id_level")
                };
                model.addRow(data);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        // untuk me refresh table
        txt_iduser.setText("");
        txt_username.setText("");
        txt_pass.setText("");
        txt_namauser.setText("");
    }

